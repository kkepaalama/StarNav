### Python class to organize OST coordinate outputs

class StarImage:
    def __init__(self, time, body, cel, wt, imu_tilt, stars, radec):
        self.time = time
        self.body = body
        self.cel = cel
        self.wt = wt
        self.imu_tilt = imu_tilt
        self.stars = stars
        self.radec = radec






